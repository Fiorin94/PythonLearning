"""
Existem argumentos que podemos colocar nas chamadas.
Como 'sep=' para definir o tipo de separação dos argumentos
E também o 'end=' para definir o que será incluso após o último argumento.

Como padrão, ele inclui um espaço para separar e a quebra de linha no final.
"""

print(12, 34, sep='-', end= '\n')
print(56, 78,sep='-', end= '\n')